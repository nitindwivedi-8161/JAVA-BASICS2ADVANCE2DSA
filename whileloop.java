public class whileloop {
    public static void main (String args []){
        
    //while loop
    /* Syntax
     * while(condition){
     * // body
     * }
     */
    int num = 1;
    while (num<=5){
        System.out.println(num);
        num += 1;


    }

    // do while
    /* 
     * 
     * do{
     * //body
     * 
     * }while(Condition);
     * 
     */

     int n=1;
     do{
        System.out.println(n);
        n++;

     }while(n<=5);


    }
    
}
